-- -------------------------------------------------------------------------
-- Drop schema procedure - drops the schema and access roles
-- -------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE DROP_SCHEMA (database_name VARCHAR, schema_name VARCHAR)
RETURNS VARCHAR
EXECUTE AS CALLER
AS
DECLARE
    ro_role := schema_name || '_RO_AR';
    rw_role := schema_name || '_RW_AR';
    full_role := schema_name || '_FULL_AR';
    SCHEMA_COUNT INTEGER;
BEGIN
    SHOW SCHEMAS IN DATABASE IDENTIFIER(:database_name);
    SELECT count(1) INTO SCHEMA_COUNT
    FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()))
    WHERE "name" = UPPER(:schema_name);

    IF (SCHEMA_COUNT = 0) THEN
        RETURN 'Schema ' || :database_name || '.' || :schema_name || ' does not exist.';
    END IF;

    -- drop schema
    EXECUTE IMMEDIATE 'DROP SCHEMA ' || :database_name || '.' || :schema_name;

    -- drop roles
    DROP DATABASE ROLE IF EXISTS IDENTIFIER(:ro_role);
    DROP DATABASE ROLE IF EXISTS IDENTIFIER(:rw_role);
    DROP DATABASE ROLE IF EXISTS IDENTIFIER(:full_role);

    RETURN 'Schema ' || :database_name || '.' || :schema_name || ' was dropped.';
END;
