-- -------------------------------------------------------------------------
-- Create schema procedure - creates a new schema and access roles within the environment
-- -------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE CREATE_SCHEMA (database_name VARCHAR, schema_name VARCHAR, env_abbr VARCHAR)
RETURNS VARCHAR
EXECUTE AS CALLER
AS
DECLARE
    RO_ROLE := database_name || '.' || schema_name || '_RO_AR';
    RW_ROLE := database_name || '.' || schema_name || '_RW_AR';
    FULL_ROLE := database_name || '.' || schema_name || '_FULL_AR';
    ENV_SYSADMIN_ROLE := env_abbr || 'SYSADMIN';
    QUALIFIED_SCHEMA_NAME := database_name || '.' || schema_name;
    RETURN_MSG VARCHAR := '';
BEGIN
-- create schema
    CREATE SCHEMA IF NOT EXISTS IDENTIFIER(:QUALIFIED_SCHEMA_NAME) WITH MANAGED ACCESS;
    RETURN_MSG := RETURN_MSG || 'Created schema ' || QUALIFIED_SCHEMA_NAME || '; ';

-- create schema access roles
    -- read only
    CREATE DATABASE ROLE IF NOT EXISTS IDENTIFIER(:RO_ROLE);
    RETURN_MSG := RETURN_MSG || 'Created role ' || RO_ROLE || '; ';

    -- read/write
    CREATE DATABASE ROLE IF NOT EXISTS IDENTIFIER(:RW_ROLE);
    RETURN_MSG := RETURN_MSG || 'Created role ' || RW_ROLE || '; ';
    -- grant ro role to rw role to maintain role hierarchy
    GRANT DATABASE ROLE IDENTIFIER(:RO_ROLE) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);

    -- full access
    CREATE DATABASE ROLE IF NOT EXISTS IDENTIFIER(:FULL_ROLE);
    RETURN_MSG := RETURN_MSG || 'Created role ' || FULL_ROLE || '; ';
    -- grant ro role to rw role to maintain role hierarchy
    GRANT DATABASE ROLE IDENTIFIER(:RW_ROLE) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    GRANT DATABASE ROLE IDENTIFIER(:FULL_ROLE) TO ROLE IDENTIFIER(:ENV_SYSADMIN_ROLE);

-- grants to access roles
    -- ro_role
    GRANT USAGE ON SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);

    GRANT SELECT ON ALL TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON ALL EXTERNAL TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON ALL VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE, READ ON ALL STAGES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON ALL FILE FORMATS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON ALL STREAMS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON ALL PROCEDURES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON ALL FUNCTIONS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON ALL MATERIALIZED VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON FUTURE TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON FUTURE EXTERNAL TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON FUTURE VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE, READ ON FUTURE STAGES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON FUTURE FILE FORMATS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON FUTURE STREAMS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON FUTURE PROCEDURES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT USAGE ON FUTURE FUNCTIONS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);
    GRANT SELECT ON FUTURE MATERIALIZED VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RO_ROLE);

    -- rw_role
    GRANT USAGE ON SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);

    GRANT INSERT, UPDATE, DELETE, TRUNCATE, REFERENCES ON FUTURE TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);
    GRANT INSERT, UPDATE, DELETE, REFERENCES ON FUTURE EXTERNAL TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);
    GRANT READ, WRITE ON FUTURE STAGES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);
    GRANT USAGE ON FUTURE STREAMLITS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);
    GRANT USAGE ON FUTURE PROCEDURES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);
    GRANT USAGE ON FUTURE FUNCTIONS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:RW_ROLE);

    -- full_role
    GRANT ALL PRIVILEGES ON SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);

    -- do not grant future ownership because it messes up some object types (eg. tasks, dynamic tables, data metric functions, etc.)
    -- use a functional role to create objects

    --GRANT OWNERSHIP ON FUTURE TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE EXTERNAL TABLES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE STAGES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE FILE FORMATS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE STREAMS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE PROCEDURES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE FUNCTIONS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE MATERIALIZED VIEWS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE SEQUENCES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE TASKS IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    --GRANT OWNERSHIP ON FUTURE PIPES IN SCHEMA IDENTIFIER(:QUALIFIED_SCHEMA_NAME) TO DATABASE ROLE IDENTIFIER(:FULL_ROLE);
    
    RETURN RETURN_MSG;
END;

